    private void tbSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSanPhamMouseClicked
        // TODO add your handling code here:
        SanPham sanPham = new SanPham();
        int row = tbSanPham.getSelectedRow();
        sanPham.setMaSanPham(Integer.parseInt(tbSanPham.getValueAt(row, 0).toString().trim()));
        sanPham.setTenSanPham(tbSanPham.getValueAt(row, 1).toString().trim());
        sanPham.setSoLuongTon(Integer.parseInt(tbSanPham.getValueAt(row, 2).toString().trim()));
        sanPham.setDonGia(Integer.parseInt(tbSanPham.getValueAt(row, 3).toString().trim()));
        XoaSuaSP ED = new XoaSuaSP(sanPham);
        ED.setVisible(true);

    }//GEN-LAST:event_tbSanPhamMouseClicked

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        dtm.setColumnIdentifiers(tieude);
        tbSanPham.setModel(dtm);
        SanPham sp = SanPhamDAO.timKiemSanPham(txtTimKiem.getText().trim());
        if (sp.getMaSanPham() == 0) {
            JOptionPane.showMessageDialog(this, "không tìm thấy sản phẩm");
            loadLSP();
        } else {
            dtm.setRowCount(0);
            Vector<Object> v = new Vector<Object>();
            v.add(sp.getMaSanPham());
            v.add(sp.getTenSanPham());
            v.add(sp.getDonGia());
            v.add(sp.getSoLuongTon());
            dtm.addRow(v);
        }

    }//GEN-LAST:event_btnTimKiemActionPerformed

