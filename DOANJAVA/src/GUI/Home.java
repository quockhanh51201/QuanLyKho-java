    private void loadLSP() {
        dtm.setColumnIdentifiers(tieude);
        tbSanPham.setModel(dtm);
        ArrayList<SanPham> LSP = SanPhamDAO.layDanhSachSanPham();
        dtm.setRowCount(0);
        for (SanPham sp : LSP) {
            Vector<Object> v = new Vector<Object>();
            v.add(sp.getMaSanPham());
            v.add(sp.getTenSanPham());
            v.add(sp.getSoLuongTon());
            v.add(sp.getDonGia());   
            dtm.addRow(v);
        }
    }

    private void LoadThongTinNhanVien() {
        txtMaNhanVien.setEnabled(false);
        NhanVien nhanVien = NhanVienDAO.layThongTinNhanVien(taiKhoan.getMa());
        txtMaNhanVien.setText(String.valueOf(nhanVien.getMaNhanVien()));
        txtNgaySinh.setText(nhanVien.getNgaySinh());
        txtSDT.setText(nhanVien.getSoDienThoai());
        txtTenNhanVien.setText(nhanVien.getHoTen());
        txtDiaChi.setText(nhanVien.getDiaChi());
    }
